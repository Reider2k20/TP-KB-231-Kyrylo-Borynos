class Functions:
    @staticmethod
    def sum(a, b):
        return a + b
    
    @staticmethod
    def sub(a, b):
        return a - b
    
    @staticmethod
    def mult(a, b):
        return a * b
    
    @staticmethod
    def div(a, b):
        return a / b
        
    @staticmethod
    def deg(a, b):
        return a ** b